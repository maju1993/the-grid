//////////////////////////////////////////////////////////////////////
// shader wierzchokw do renderingu tekstu z punktw
//////////////////////////////////////////////////////////////////////
const char *stringVertexShaderSrc[] =
{
    //////////////////////////////////////////////////////////////////////
    // GLSL 1.50
    //////////////////////////////////////////////////////////////////////
    "#version 150                                                       \n"

    //////////////////////////////////////////////////////////////////////
    // iloczyn macierzy modelu-widoku i macierzy rzutowania
    //////////////////////////////////////////////////////////////////////
    "uniform mat4 modelViewProjectionMatrix;                            \n"

    //////////////////////////////////////////////////////////////////////
    // wsprzdne punktu
    //////////////////////////////////////////////////////////////////////
    "in vec4 inPosition;                                                \n"

    "void main()                                                        \n"
    "{                                                                  \n"
        // przeksztacenie wierzchokw
        "gl_Position = modelViewProjectionMatrix * inPosition;          \n"

        // wielko punktu
        "gl_PointSize = 1.0;                                            \n"
    "}                                                                  \n"
};

//////////////////////////////////////////////////////////////////////
// shader fragmentw do renderingu tekstu z punktw
//////////////////////////////////////////////////////////////////////
const char *stringFragmentShaderSrc[] =
{
    //////////////////////////////////////////////////////////////////////
    // GLSL 1.50
    //////////////////////////////////////////////////////////////////////
    "#version 150                                                       \n"

    //////////////////////////////////////////////////////////////////////
    // kolor fragmentu
    //////////////////////////////////////////////////////////////////////
    "uniform vec4 inColor;                                              \n"

    //////////////////////////////////////////////////////////////////////
    // wyjciowy kolor fragmentu
    //////////////////////////////////////////////////////////////////////
    "out vec4 outColor;                                                 \n"

    "void main()                                                        \n"
    "{                                                                  \n"
        // kolor fragmentu
        "outColor = inColor;                                            \n"
    "}                                                                  \n"
};

//////////////////////////////////////////////////////////////////////
// znaki 8x16 pikseli, maksymalnie pojemno bufora wierzchokw
// obejmuje 256 znakw
//////////////////////////////////////////////////////////////////////
const int maxStringLength = 8*16*2*256; 

//////////////////////////////////////////////////////////////////////
// identyfikator obiektu programu
//////////////////////////////////////////////////////////////////////
GLuint stringShader = 0;

//////////////////////////////////////////////////////////////////////
// identyfikator obiektu bufora z danymi tablic wierzchokw
//////////////////////////////////////////////////////////////////////
GLuint stringPositionBuffer = 0;

//////////////////////////////////////////////////////////////////////
// identyfikator obiektu tablic wierzchokw
//////////////////////////////////////////////////////////////////////
GLuint stringVAO = 0;

//////////////////////////////////////////////////////////////////////
// inicjacja mechanizmw uywanych podczas renderingu tekstu
// color - numer koloru fragmentu, do ktrego kierowane s dane fragmentw
//////////////////////////////////////////////////////////////////////
void InitDrawText( GLuint color )
{
    // wczytanie i przygotowanie obsugi shadera rysujcego napisy
    if( !stringShader )
    {
        AttachVertexFragmentShader( stringShader, stringVertexShaderSrc, stringFragmentShaderSrc );
        glBindFragDataLocation( stringShader, color, "outColor" );
        LinkProgram( stringShader );
    }

    // obiekt tablic wierzchokw z buforem tablic wierzchokw
    // na wsprzdne punktw z tekstem (bez adowania danych)
    if( !stringPositionBuffer && !stringVAO )
    {
        // generowania identyfikatora obiektu tablic wierzchokw
        glGenVertexArrays( 1, &stringVAO );

        // utworzenie obiektu tablic wierzchokw
        glBindVertexArray( stringVAO );

        // generowanie VBO
        glGenBuffers( 1, &stringPositionBuffer );
        glBindBuffer( GL_ARRAY_BUFFER, stringPositionBuffer );
        glBufferData( GL_ARRAY_BUFFER, maxStringLength * sizeof( GLfloat ), NULL, GL_DYNAMIC_DRAW );
        GLuint stringPositionLoc = glGetAttribLocation( stringShader, "inPosition" );
        glVertexAttribPointer( stringPositionLoc, 2, GL_FLOAT, GL_FALSE, 0, NULL );
        glEnableVertexAttribArray( stringPositionLoc );

        // wyczenie obiektu tablic wierzchokw
        glBindVertexArray( 0 );
    }
}

//////////////////////////////////////////////////////////////////////
// funkcja rysujca napis w wybranym miejscu
// x, y - wsprzdne okienkowe pocztku napisu
// str - tekst napisu
// color - kolor napisu, domylnie clBlack
// font - czcionka napisu, 8x16 pikseli, od znaku nr 32 (spacji),
//        domylnie font8x16CP1250
//////////////////////////////////////////////////////////////////////
void DrawText8x16( const int x, const int y, const std::string &str, const GLfloat *color, const unsigned char font[][16] )
{
    // pobranie rozmiarw okna renderingu
    GLint viewport[4];
    glGetIntegerv( GL_VIEWPORT, viewport );

    // przygotowanie macierzy rzutowania prostoktnego 2D; wykorzystujemy
    // wycznie macierz rzutowania, bowiem macierz modelu-widoku jest
    // w tym przypadku macierz jednostkow
    matrix4x4<> projectionMatrix;
    projectionMatrix.LoadIdentity();
    projectionMatrix.Ortho2D( 0, viewport[2], 0, viewport[3] );
    projectionMatrix.transpose();

    // generowanie wsprzdnych punktw tworzcych napis
    int pos = 0;
    GLfloat buf[maxStringLength];
    for( unsigned int i = 0; i < str.length(); i++ )
    {
        // przygotowanie danych pojedynczego znaku
        int chr = static_cast<unsigned char>( str[i] ) - 32;
        for( int row = 0; row < 16; row++ )
        {
            // pobranie danych pojedynczego wiersza pikseli w znaku
            // z przesuniciem o 32 jednostki (dane znakw zaczynaj si od spacji)
            unsigned int r = font[chr][row];
            for( int col = 0; col < 8; col++ )
            {
                // test obecnoci poszczeglnych bitw oraz test pojemoci bufora
                if( ((r >> col) & 0x01) == 0x01 && pos < maxStringLength - 1 )
                {
                    buf[pos + 0] = static_cast< GLfloat >( x + i*8 + 7 - col );
                    buf[pos + 1] = static_cast< GLfloat >( y + row );
                    pos += 2;
                }
            }
        }
    }

    // sprawdzenie czy wczone jest generowania wielkoci
    // punktu w shaderze wierzchokw/geometrii
    GLboolean programPointSize = glIsEnabled( GL_PROGRAM_POINT_SIZE );
    if( !programPointSize )
        glEnable( GL_PROGRAM_POINT_SIZE );

    // wybr obiektu tablic wierzchokw
    glBindVertexArray( stringVAO );

    // wybr obiektu bufora wierzchokw
    glBindBuffer( GL_ARRAY_BUFFER, stringPositionBuffer );

    // zaadowanie przygotowych danych obiektu bufora wierzchokw
    glBufferSubData( GL_ARRAY_BUFFER, 0, pos * sizeof( GLfloat ), buf );

    // wczenie programu cieniowania
    glUseProgram( stringShader );

    // zaadowanie zmiennych jednorodnych - iloczynu macierzy 
    // modelu-widoku i rzutowania oraz koloru punktu
    glUniformMatrix4fv( glGetUniformLocation( stringShader, "modelViewProjectionMatrix" ), 1, GL_FALSE, projectionMatrix.data() );
    glUniform4fv( glGetUniformLocation( stringShader, "inColor" ), 1, color );

    // narysowanie danych zawartych w tablicach wierzchokw
    glDrawArrays( GL_POINTS, 0, pos/2 );

    // wyczenie programu cieniowania
    glUseProgram( 0 );

    // wyczenie obiektu tablic wierzchokw
    glBindVertexArray( 0 );

    // w razie potrzeby wyczamy generowania wielkoci
    // punktu w shaderze wierzchokw/geometrii
    if( !programPointSize )
        glDisable( GL_PROGRAM_POINT_SIZE );
}

//////////////////////////////////////////////////////////////////////
// usunicie mechanizmw uywanych podczas renderingu tekstu
//////////////////////////////////////////////////////////////////////
void DeleteDrawText()
{
    DeleteShaders( stringShader );
    glDeleteProgram( stringShader );
    glDeleteBuffers( 1, &stringPositionBuffer );
    glDeleteVertexArrays( 1, &stringVAO );

    // wyzerowanie identyfikatorw
    stringShader = 0;
    stringPositionBuffer = 0;
    stringVAO = 0;
}
